package kr.spring.ch05.vo;

public class SearchVO {
	private String type;
	private String query;

	public void setType(String type) {
		this.type = type;
	}

	public void setQuery(String query) {
		this.query = query;
	}

	public String getType() {
		return type;
	}

	public String getQuery() {
		return query;
	}

}
